from pygame import *
from random import randint


init()
W, H = 600, 700

window = display.set_mode((W, H))
display.set_caption('Space Shooter')

bg = transform.scale(image.load('images/galaxy.jpg'), (W, H))

clock = time.Clock()

class GameSprite(sprite.Sprite):
    def __init__(self, x, y, width, height, speed, img):
        super().__init__()
        self.width = width
        self.height = height
        self.speed = speed
        self.image = transform.scale(image.load(img), (width, height))
        self.rect = self.image.get_rect()
        self.rect. x = x
        self.rect. y = y

    def draw(self):
        window.blit(self.image, (self.rect.x, self.rect.y))

class Player(GameSprite):
    def move(self):
        keys_pressed = key.get_pressed()
        if keys_pressed[K_a] and self.rect.x > 0:
            self.rect.x -= self.speed
        if keys_pressed[K_d] and self.rect.x < W - self.width:
            self.rect.x += self.speed

player = Player(W/2, H - 100, 80, 100, 15, 'images/rocket.png')


life = 3
killed = 0 
skipped = 0


class Enemy(GameSprite):
    def update(self):
        global killed 
        self.rect.y += self.speed
        if self.rect.y > H - self.height:
            self.rect.x = randint(0, H - self.width)
            self.rect.y = 0
            killed += 1

enemies = sprite.Group()
for i in range(5):
    enemy = Enemy(randint(0, H), 0, 70, 35, 2, 'images/ufo.png' )
    enemies.add(enemy)



game = True
while game:
    for e in event.get():
        if e.type == QUIT:
            game = False

    window.blit(bg, (0, 0))
    player.draw()
    player.move()
    enemies.draw(window)
    enemies.update()
    display.update()
    clock.tick(60)






















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































